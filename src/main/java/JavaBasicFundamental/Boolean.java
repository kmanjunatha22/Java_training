package JavaBasicFundamental;

public class Boolean {
    public static void main(String[] args) {

                int x = 5, y = 8;
                boolean z=x==y;
                System.out.println("z: " + z);  // false equals to
                System.out.println("x != y: " + (x != y));  // true not equals to
                System.out.println("x > y: " + (x > y));    // false greater than
                System.out.println("x < y: " + (x < y));    // true less than
                System.out.println("x >= y: " + (x >= y));  // false greater than equals to
                System.out.println("x <= y: " + (x <= y));  // true less than equals to



    }
}
